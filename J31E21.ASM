	ORG	0000H

CONA	EQU	0F800H
CONB	EQU	0F801H

EXAM:	MOV	R7,#8
	MOV	R6,#0

EXAM1:	MOV	DPTR,#CONB
	MOVX	A, @DPTR
	ANL	A, #11111000B
	ORL	A,R6
	MOVX	@DPTR,A

	MOV	R5,#8
	MOV	P0,#1
EXAM2:	MOV	DPTR,#CONA
	MOV	A,P0
	MOVX	@DPTR,A
	RL	A
	MOV	P0,A
	CALL	EXAMD
	DJNZ	R5,EXAM2

	MOV	A,R6
	ORL	A,#20H
	MOV	R2,A
	MOV	R3,#08H
	INC	R6
	DJNZ	R7,EXAM1
	SJMP	EXAM

EXAMD:	MOV	R3,#01H
EXAMD1:	MOV	R2,#01H
	DJNZ	R2,$
	DJNZ	R3,EXAMD1
	RET

